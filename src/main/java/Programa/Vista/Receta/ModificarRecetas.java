    private List<Document> convertirIngredientes(String ingredientesInput) {
    List<Document> ingredientes = new ArrayList<>();

    // Suponiendo que los ingredientes están separados por comas
    String[] ingredientesArray = ingredientesInput.split(",");

    for (String ingredienteInfo : ingredientesArray) {
        // Suponiendo que el formato es: "NombreIngrediente (Cantidad)"
        String[] partes = ingredienteInfo.split("\\(");
        if (partes.length == 2) {
            String nombre = partes[0].trim();
            String cantidad = partes[1].replace(")", "").trim();

            // Crear un documento para cada ingrediente
            Document ingredienteDoc = new Document("name_spanish", nombre)
                                            .append("quantity", cantidad);
            ingredientes.add(ingredienteDoc);
        }
    }
    return ingredientes;
}
    
    
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // Validar los campos del formulario
        if (nombreText.getText().isEmpty() || ingredientesText.getText().isBlank()
            || tiempoText.getText().isBlank() || pasosTextArea.getText().isBlank()
            || ocasionText.getText().isBlank() || chefText.getText().isBlank()) {

            JOptionPane.showMessageDialog(this, "Escriba lo que se le solicita", "No se ha completado los datos", JOptionPane.WARNING_MESSAGE);
        } else {
            // Determinar si se usa en festividades
            boolean festividad = si.isSelected();

            // Obtener los valores de los campos de la interfaz
            String nombre = nombreText.getText();
            String ingredientesInput = ingredientesText.getText();
            List<String> pasos = convertirPasos(pasosTextArea.getText());
            String tiempoPreparacion = tiempoText.getText();
            String ocasion = ocasionText.getText();
            String quienPrepara = chefText.getText();

            // Convertir los ingredientes a List<Document>
            List<Document> ingredientes = convertirIngredientes(ingredientesInput);

            // Crear instancia del controlador
            Recetas controladorRecetas = new Recetas();

            // Modificar la receta en la base de datos
            controladorRecetas.modificarReceta(
                nombre, ingredientes, pasos, tiempoPreparacion, ocasion, quienPrepara, festividad
            );

            JOptionPane.showMessageDialog(this, "Receta modificada correctamente", "Modificación exitosa", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    // Función para convertir los pasos
    private List<String> convertirPasos(String pasosInput) {
        // Suponiendo que los pasos están separados por saltos de línea
        String[] pasosArray = pasosInput.split("\n");
        return new ArrayList<>(Arrays.asList(pasosArray));
    }

