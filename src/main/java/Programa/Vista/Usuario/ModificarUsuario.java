    // Botón para buscar usuario
    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        if (!usuarioControlador.existeUsuario(cedulaText.getText())) {
            JOptionPane.showMessageDialog(this, "Usuario no existe", "Error al buscar", JOptionPane.WARNING_MESSAGE);
        } else {
            if (cedulaText.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Escriba lo que se le solicita", "Datos incompletos", JOptionPane.WARNING_MESSAGE);
            } else {
                // Obtener los datos del usuario con el controlador
                Document usuario = usuarioControlador.buscarUsuarioPorCedula(cedulaText.getText());
                if (usuario != null) {
                    nombreText.setText(usuario.getString("Nombre"));
                    contrasennaText.setText(usuario.getString("Contraseña"));
                    if ("Investigador".equals(usuario.getString("Tipo"))) {
                        investigador.setSelected(true);
                    } else {
                        colaborador.setSelected(true);
                    }
                    JOptionPane.showMessageDialog(this, "Información encontrada", "Información del Usuario", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed

