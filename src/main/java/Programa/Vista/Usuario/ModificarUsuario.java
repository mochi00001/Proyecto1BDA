    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        String tipo = "";
        
        if (cedulaText.getText().isEmpty() || cedulaText.getText().isBlank()
            || nombreText.getText().isBlank() || nombreText.getText().isEmpty()
            || contrasennaText.getText().isBlank() || contrasennaText.getText().isEmpty()){

            JOptionPane.showMessageDialog(this, "Escriba lo que se le solicita", "No se a completado los datos", JOptionPane.WARNING_MESSAGE);
            
        } else {
            if (colaborador.isSelected()){
                tipo ="Colaborador";
            }else{
                tipo ="Investigador";
            }
            if(!usuarioControlador.existeUsuario(cedulaText.getText())){
                JOptionPane.showMessageDialog(this, "Usuario no existe", "Error al registrar", JOptionPane.WARNING_MESSAGE);
            }else{
                usuarioControlador.modificarUsuario(cedulaText.getText(),nombreText.getText(),contrasennaText.getText(),tipo);
                JOptionPane.showMessageDialog(this, "Información modificada", "Datos registrados", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

